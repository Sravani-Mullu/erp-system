const statuses = ["Pending", "Shipped", "Delivered"];

let orderIdCounter = 40;

const generateRandomDate = (start, end) => {
  return new Date(
    start.getTime() + Math.random() * (end.getTime() - start.getTime())
  );
};

export const generateRandomOrder = () => {
  const randomProductIndex = Math.floor(Math.random() * listOfProducts.length);
  const randomProduct = listOfProducts[randomProductIndex];
  const randomQuantity = Math.floor(Math.random() * 5) + 1; // Random quantity between 1 and 5
  const randomDate = generateRandomDate(
    new Date(2023, 11, 1),
    new Date(2024, 2, 31)
  );
  const randomAmount = randomQuantity * randomProduct.price;
  const orderId = `ORD-${String(++orderIdCounter).padStart(3, "0")}`;
  const randomNameIndex = Math.floor(Math.random() * names.length);
  const randomStatusIndex = Math.floor(Math.random() * statuses.length);

  return {
    id: orderIdCounter,
    ordernumber: orderId,
    customername: names[randomNameIndex],
    orderdate: randomDate.toISOString().split("T")[0],
    status: statuses[randomStatusIndex],
    amount: randomAmount,
    ordereditem: randomProduct.name,
    quantity: randomQuantity,
    shippingaddress: "Random Address",
    shippingmethod: "Standard",
    deliverydate: generateRandomDate(randomDate, new Date(2024, 2, 31))
      .toISOString()
      .split("T")[0],
  };
};
